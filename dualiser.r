pdual <- function(P=c(1,1,1), col="red"){

  a <- P[1]; b <- P[2]; c <- P[3]

  if(a==0 & b==0 & c==0){
    return("Point not in RP^2")
  }
  else{
    if(a==0 & b==0){
      return("Line at infinity")
    }
    else{
    if(b==0){
      x <- seq(from=-50, to=50, by=1/1000)
      l <- (x-x) + -c/a
      lines(x=l, y=seq(from=-50,to=50,by=1/1000), col=col)
    }
    else{
      x <- seq(from=-50, to=50, by=1/1000)
      l <- -(a*x + c)/b
      lines(x=seq(from=-50, to=50, by=1/1000), y=l, col=col)
    }
    }
  }
}

pplot <- function(P=c(1,1,1), col="red"){
  a <- P[1]; b <- P[2]; c <- P[3];
  if(c==0){
    return("Not an affine point")
  }
  else{
    points(x=a/c,y=b/c,col=col)
  }
}


